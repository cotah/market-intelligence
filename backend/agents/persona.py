def build_system(instruction: str, *, portfolio_context: bool = False) -> str:
    """Monta o system prompt de um agente de coleta.

    Args:
        instruction: instrução específica do agente (ancora o contrato de saída).
        portfolio_context: injeta o contexto de produto da empresa. Deixe False
            para pesquisa de mercado aberta.
    """
    parts = [BUSCA_SYSTEM_CORE]
    if portfolio_context:
        parts.append(BUSCA_SYSTEM_CONTEXT)
    parts.append(instruction)
    return "\n\n".join(parts)
